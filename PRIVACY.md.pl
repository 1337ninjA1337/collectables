# Collectables — Polityka prywatności

_Data wejścia w życie: 2026-07-04_

To jest tłumaczenie sekcji „Raportowanie awarii i diagnostyka" (Sentry)
publicznej polityki prywatności Collectables, udostępniane zgodnie z
art. 12 RODO. Pełna wersja polityki jest dostępna w języku angielskim —
skorzystaj z przełącznika języka u góry strony.

## Raportowanie awarii i diagnostyka

> **Raportowanie awarii i diagnostyka.** Collectables korzysta z Sentry
> (Functional Software, Inc., d/b/a Sentry — https://sentry.io) jako
> podmiotu podprzetwarzającego dane w celu zbierania nieobsłużonych
> wyjątków, śladów stosu oraz kontekstu urządzenia/systemu operacyjnego
> niezbędnego do ich debugowania. Raport o awarii zawiera Twój
> identyfikator użytkownika Supabase, abyśmy mogli powiązać raporty z
> kontem, jednak pola umożliwiające identyfikację osoby (adres e-mail,
> adres IP, pliki cookie i nagłówki `Authorization`) są usuwane po
> stronie klienta przed wysłaniem. Dane diagnostyczne są przechowywane
> przez Sentry maksymalnie 90 dni zgodnie z ich domyślną polityką
> przechowywania i nigdy nie są sprzedawane, udostępniane brokerom danych
> ani wykorzystywane do reklam. Raportowanie awarii możesz wyłączyć w
> dowolnym momencie w sekcji **Ustawienia → Diagnostyka i raporty o
> awariach**; po wyłączeniu żadne zdarzenia nie opuszczają urządzenia.
> Praktyki prywatności Sentry i ich DPA znajdziesz pod adresami
> https://sentry.io/privacy/ i https://sentry.io/legal/dpa/.
