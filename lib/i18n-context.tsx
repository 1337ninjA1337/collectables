const translations: Record<AppLanguage, TranslationMap> = { en, ru, be, pl, de, es };

const languageOptions: { code: AppLanguage; label: string }[] = [
  { code: "ru", label: "Русский" },
  { code: "en", label: "English" },
  { code: "be", label: "Беларуская" },
  { code: "pl", label: "Polski" },
  { code: "de", label: "Deutsch" },
  { code: "es", label: "Español" },
];

const I18nContext = createContext<{
  language: AppLanguage;
  ready: boolean;
  setLanguage: (language: AppLanguage) => Promise<void>;
  t: (key: TranslationKey, params?: TranslationParams) => string;
  languageOptions: { code: AppLanguage; label: string }[];
} | null>(null);

export function I18nProvider({ children }: React.PropsWithChildren) {
  const [language, setLanguageState] = useState<AppLanguage>("ru");
  const [ready, setReady] = useState(false);

  useEffect(() => {
    let active = true;

    AsyncStorage.getItem(STORAGE_KEY)
      .then((value) => {
        if (!active) {
          return;
        }
        if (value && languageOptions.some((option) => option.code === value)) {
          setLanguageState(value as AppLanguage);
        }
      })
      .finally(() => {
        if (active) {
          setReady(true);
        }
      });

    return () => {
      active = false;
    };
  }, []);

  const value = useMemo(
    () => ({
      language,
      ready,
      setLanguage: async (nextLanguage: AppLanguage) => {
        setLanguageState(nextLanguage);
        await AsyncStorage.setItem(STORAGE_KEY, nextLanguage);
      },
      t: (key: TranslationKey, params?: TranslationParams) => {
        const entry = translations[language][key] ?? translations.en[key];
        return typeof entry === "function" ? entry(params) : entry;
      },
      languageOptions,
    }),
    [language, ready],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
